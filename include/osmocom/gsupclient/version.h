#pragma once

#define LIBOSMO_GSUP_CLIENT_VERSION UNKNOWN
#define LIBOSMO_GSUP_CLIENT_VERSION_STR "UNKNOWN"

#define LIBOSMO_GSUP_CLIENT_VERSION_MAJOR UNKNOWN
#define LIBOSMO_GSUP_CLIENT_VERSION_MINOR UNKNOWN
#define LIBOSMO_GSUP_CLIENT_VERSION_PATCH UNKNOWN

#define LIBOSMO_GSUP_CLIENT_VERSION_GREATER_EQUAL(major, minor, patch) \
	(LIBOSMO_GSUP_CLIENT_VERSION_MAJOR > (major) || \
	 (LIBOSMO_GSUP_CLIENT_VERSION_MAJOR == (major) && \
	  LIBOSMO_GSUP_CLIENT_VERSION_MINOR > (minor)) || \
	 (LIBOSMO_GSUP_CLIENT_VERSION_MAJOR == (major) && \
	  LIBOSMO_GSUP_CLIENT_VERSION_MINOR == (minor) && \
	  LIBOSMO_GSUP_CLIENT_VERSION_PATCH >= (patch)))
